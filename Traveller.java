public class Traveller {
    private int TravelerNumber;
    private int TravelersTime;

    public Traveller(int valNum, int valTime) {
        this.TravelerNumber = valNum;
        this.TravelersTime = valTime;
    }

    public int getTravelerNumber() {
        return TravelerNumber;
    }

    public int getTravelersTime() {
        return TravelersTime;
    }


}
